package pe.edu.upc.dsd.taxiempresarial.model;

public class Sede {
	private String cod_sede;
	private String desc_sede;
	private String direc_sede;
	private String telf_sede;
	private String cod_distrito;
	private String estado;
	
	public Sede() {		
	}
	
	public String getCod_sede() {
		return cod_sede;
	}
	public void setCod_sede(String cod_sede) {
		this.cod_sede = cod_sede;
	}
	public String getDesc_sede() {
		return desc_sede;
	}
	public void setDesc_sede(String desc_sede) {
		this.desc_sede = desc_sede;
	}
	public String getDirec_sede() {
		return direc_sede;
	}
	public void setDirec_sede(String direc_sede) {
		this.direc_sede = direc_sede;
	}
	public String getTelf_sede() {
		return telf_sede;
	}
	public void setTelf_sede(String telf_sede) {
		this.telf_sede = telf_sede;
	}
	public String getCod_distrito() {
		return cod_distrito;
	}
	public void setCod_distrito(String cod_distrito) {
		this.cod_distrito = cod_distrito;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	
	
}
